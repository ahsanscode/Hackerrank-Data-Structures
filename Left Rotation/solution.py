def rotateLeft(d, arr):
    # Write your code here
    arrayx = [0]*d
    for var in range(d):
        arrayx[var] = arr[var]
    for var in range(len(arr)-d):
        arr[var] = arr[var+d]
    for var in range(d):
        arr[(len(arr) - d) + var ] = arrayx[var]
    return arr
        

